/**
 * Cross-platform desktop/GUI environment setup for Claude Desktop and shells.
 */

import { execSync } from 'child_process';
import fs from 'fs';
import path from 'path';
import os from 'os';
import { logger } from '../lib/logger.js';

const PROXY_URL = 'http://127.0.0.1:3456';

function configDir(): string {
  return path.join(os.homedir(), '.claude', 'claude-code-proxy');
}

/** Write env.sh sourced by shell profiles and systemd units. */
export function writeEnvSh(): void {
  const dir = configDir();
  const envPath = path.join(dir, 'env.sh');
  const modelsPath = path.join(dir, 'models.sh');

  const lines = [
    '#!/bin/bash',
    '# Auto-generated by Claude Code Proxy',
    `export ANTHROPIC_BASE_URL="${PROXY_URL}"`,
    `[ -f "${modelsPath}" ] && source "${modelsPath}"`,
  ];

  if (!fs.existsSync(dir)) {
    fs.mkdirSync(dir, { recursive: true, mode: 0o700 });
  }
  fs.writeFileSync(envPath, lines.join('\n') + '\n', { mode: 0o644 });
  logger.info({ path: envPath }, 'Wrote cross-platform env.sh');
}

function setupLaunchctlEnv(): void {
  if (process.platform !== 'darwin') return;

  try {
    const current = execSync(
      'launchctl getenv ANTHROPIC_BASE_URL 2>/dev/null || true',
      { encoding: 'utf-8', timeout: 2000 },
    ).trim();

    if (current !== PROXY_URL && current !== 'http://localhost:3456') {
      execSync(`launchctl setenv ANTHROPIC_BASE_URL ${PROXY_URL}`, { timeout: 2000 });
      logger.info('Set ANTHROPIC_BASE_URL via launchctl for GUI apps');
    }
  } catch (err) {
    logger.warn(
      { err: err instanceof Error ? err.message : String(err) },
      'Could not set launchctl env',
    );
  }
}

function setupLinuxUserEnv(): void {
  if (process.platform !== 'linux') return;

  const bashrc = path.join(os.homedir(), '.bashrc');
  const marker = '# claude-code-proxy env';
  const snippet = `\n${marker}\n[ -f "${path.join(configDir(), 'env.sh')}" ] && source "${path.join(configDir(), 'env.sh')}"\n`;

  try {
    if (fs.existsSync(bashrc)) {
      const content = fs.readFileSync(bashrc, 'utf-8');
      if (!content.includes(marker)) {
        fs.appendFileSync(bashrc, snippet, { mode: 0o644 });
        logger.info('Appended env.sh source to ~/.bashrc');
      }
    }
  } catch (err) {
    logger.warn(
      { err: err instanceof Error ? err.message : String(err) },
      'Could not update ~/.bashrc',
    );
  }
}

export function setupDesktopEnv(): void {
  writeEnvSh();
  setupLaunchctlEnv();
  setupLinuxUserEnv();
}

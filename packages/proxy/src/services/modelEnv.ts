/**
 * ModelEnvService — writes ANTHROPIC_DEFAULT_* env vars to ~/.claude-code-proxy/models.sh
 * Auto-updates when user changes Model Mapping in the UI.
 * Claude Code reads these env vars to show custom models in the /model picker.
 */

import { writeFileSync, existsSync, mkdirSync } from 'fs';
import { homedir } from 'os';
import { join } from 'path';
import { providerService } from './provider.js';

const CONFIG_DIR = join(homedir(), '.claude-code-proxy');
const ENV_FILE = join(CONFIG_DIR, 'models.sh');

/**
 * Write model env vars file based on current route configuration
 */
export function writeModelEnvFile(): void {
  try {
    const routes = providerService.getRoutes();

    // Build env vars from actual route target models
    const opusRoute = routes.find(r => r.claudeTier === 'opus');
    const sonnetRoute = routes.find(r => r.claudeTier === 'sonnet');
    const haikuRoute = routes.find(r => r.claudeTier === 'haiku');

    const opusModel = opusRoute?.targetModel || '';
    const sonnetModel = sonnetRoute?.targetModel || '';
    const haikuModel = haikuRoute?.targetModel || '';

    // Default = sonnet mapped model
    const defaultModel = sonnetModel || opusModel || haikuModel || '';

    const lines = [
      '#!/bin/bash',
      '# Auto-generated by Claude Code Proxy',
      '# Updates when Model Mapping is saved in the UI',
      `export ANTHROPIC_DEFAULT_MODEL="${defaultModel}"`,
      `export ANTHROPIC_DEFAULT_OPUS_MODEL="${opusModel}"`,
      `export ANTHROPIC_DEFAULT_SONNET_MODEL="${sonnetModel}"`,
      `export ANTHROPIC_DEFAULT_HAIKU_MODEL="${haikuModel}"`,
    ];

    // Ensure directory exists
    if (!existsSync(CONFIG_DIR)) {
      mkdirSync(CONFIG_DIR, { recursive: true });
    }

    writeFileSync(ENV_FILE, lines.join('\n') + '\n', { mode: 0o644 });
    console.log(`[ModelEnv] Wrote ${ENV_FILE}`);
  } catch (error) {
    console.error('[ModelEnv] Error writing env file:', error);
  }
}

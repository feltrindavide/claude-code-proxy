/**
 * ModelEnvService — writes ANTHROPIC_DEFAULT_* env vars to ~/.claude/claude-code-proxy/models.sh
 * Auto-updates when user changes Model Mapping in the UI.
 * Claude Code reads these env vars for the /model picker and automode tier resolution.
 */

import { writeFileSync, existsSync, mkdirSync } from 'fs';
import { homedir } from 'os';
import { join } from 'path';
import { providerService } from './provider.js';

/** Claude tier IDs used when no route is configured for a tier. */
export const CLAUDE_TIER_IDS = {
  opus: 'claude-opus-4-20250514',
  sonnet: 'claude-sonnet-4-20250514',
  haiku: 'claude-haiku-4-20250514',
  fable: 'claude-fable-5',
} as const;

function configDir(): string {
  return join(homedir(), '.claude', 'claude-code-proxy');
}

function envFile(): string {
  return join(configDir(), 'models.sh');
}

/** Short label for picker display (e.g. google/gemma-4-31b-it → gemma-4-31b-it). */
export function shortModelLabel(targetModel: string): string {
  const slash = targetModel.lastIndexOf('/');
  return slash >= 0 ? targetModel.slice(slash + 1) : targetModel;
}

function pushModelEnv(
  lines: string[],
  prefix: 'OPUS' | 'SONNET' | 'HAIKU' | 'FABLE',
  targetModel: string,
): void {
  lines.push(`export ANTHROPIC_DEFAULT_${prefix}_MODEL="${targetModel}"`);
  lines.push(`export ANTHROPIC_DEFAULT_${prefix}_MODEL_NAME="${shortModelLabel(targetModel)}"`);
}

/**
 * Write model env vars file based on current route configuration.
 * Maps Claude Code tiers (opus/sonnet/haiku/fable) to upstream target models.
 */
export function writeModelEnvFile(): void {
  try {
    const routes = providerService.getRoutes();

    const opusRoute = routes.find((r) => r.claudeTier === 'opus');
    const sonnetRoute = routes.find((r) => r.claudeTier === 'sonnet');
    const haikuRoute = routes.find((r) => r.claudeTier === 'haiku');
    const fableRoute = routes.find((r) => r.claudeTier === 'fable');

    const opusModel = opusRoute?.targetModel || '';
    const sonnetModel = sonnetRoute?.targetModel || '';
    const haikuModel = haikuRoute?.targetModel || '';
    const fableModel = fableRoute?.targetModel || opusModel || sonnetModel || haikuModel || '';

    const defaultModel =
      sonnetModel || opusModel || haikuModel || CLAUDE_TIER_IDS.sonnet;

    const lines = [
      '#!/bin/bash',
      '# Auto-generated by Claude Code Proxy',
      '# Updates when Model Mapping is saved in the UI',
      `export ANTHROPIC_DEFAULT_MODEL="${defaultModel}"`,
    ];

    if (opusModel) pushModelEnv(lines, 'OPUS', opusModel);
    if (sonnetModel) pushModelEnv(lines, 'SONNET', sonnetModel);
    if (haikuModel) pushModelEnv(lines, 'HAIKU', haikuModel);
    if (fableModel) pushModelEnv(lines, 'FABLE', fableModel);

    if (!existsSync(configDir())) {
      mkdirSync(configDir(), { recursive: true });
    }

    writeFileSync(envFile(), lines.join('\n') + '\n', { mode: 0o644 });
    console.log(`[ModelEnv] Wrote ${envFile()}`);
  } catch (error) {
    console.error('[ModelEnv] Error writing env file:', error);
  }
}
